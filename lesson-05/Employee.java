
public class Employee {
    private String name;
    private int age;
    private String workpost;
    private int salary;
    private String email;
    private long phone;
	
    public Employee(String name, int age, String workpost, int salary, String email, long phone) {
        this.name = name;
        this.age = age;
        this.workpost = workpost;
        this.salary = salary;
        this.email = email;
        this.phone = phone;
    }	
}